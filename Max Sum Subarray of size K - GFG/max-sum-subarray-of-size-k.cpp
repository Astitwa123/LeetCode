class Solution{   
public:
    long maximumSumSubarray(int K, vector<int> &Arr , int N){
        // code here 
        long ans=0,sol=0;
        int n=N;
        for(int i=0;i<n;i++)
        {
            ans+=Arr[i];
            if(i>=K)
            {
                ans-=Arr[i-K];
                
            }
            if(i>=K-1)sol=max(ans,sol);
        }
        return sol;
    }
};

